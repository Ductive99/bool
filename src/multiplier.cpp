#include "bool.h"

u32 multiplier(u32 a, u32 b) {
	u32 result = 0;

	while (b > 0) {
		if (b & 1) {
			result = adder(result, a);
		}
		b >>= 1;
		a <<= 1;
	}
	return (result);
}
